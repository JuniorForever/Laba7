package Runner;

public class IntegralParameters {

    private int upperIntegralBound;
    private int lowerIntegralBound;
    private int accuracy;

    public int getUpperIntegralBound() {
        return upperIntegralBound;
    }

    public void setUpperIntegralBound(int upperIntegralBound) {
        this.upperIntegralBound = upperIntegralBound;
    }

    public int getLowerIntegralBound() {
        return lowerIntegralBound;
    }

    public void setLowerIntegralBound(int lowerIntegralBound) {
        this.lowerIntegralBound = lowerIntegralBound;
    }

    public int getAccuracy() {
        return accuracy;
    }

    public void setAccuracy(int accuracy) {
        this.accuracy = accuracy;
    }
}
